def traceback(matrix, seq1, seq2, i, j, aligned_seq1, aligned_seq2, aligned_pairs):
    gap = 9

    if i == 0:
        a_seq1 = (j * '-') + aligned_seq1
        a_seq2 = seq2[:j] + aligned_seq2
        aligned_pairs.append((a_seq1, a_seq2))
        return

    if j == 0:
        a_seq1 = seq1[:i] + aligned_seq1
        a_seq2 = (i * '-') + aligned_seq2
        aligned_pairs.append((a_seq1, a_seq2))
        return

    sigma = PAM250[seq1[i - 1]][seq2[j - 1]]
    if matrix[i][j] - sigma == matrix[i - 1][j - 1]:
        a_seq1 = seq1[i - 1] + aligned_seq1
        a_seq2 = seq2[j - 1] + aligned_seq2
        traceback(matrix, seq1, seq2, i - 1, j - 1, a_seq1, a_seq2, aligned_pairs)

    if matrix[i][j] + gap == matrix[i - 1][j]:
        a_seq1 = seq1[i - 1] + aligned_seq1
        a_seq2 = '-' + aligned_seq2
        traceback(matrix, seq1, seq2, i - 1, j, a_seq1, a_seq2, aligned_pairs)

    if matrix[i][j] + gap == matrix[i][j - 1]:
        a_seq1 = '-' + aligned_seq1
        a_seq2 = seq2[j - 1] + aligned_seq2
        traceback(matrix, seq1, seq2, i, j - 1, a_seq1, a_seq2, aligned_pairs)


def main():
    seq1 = input()
    seq2 = input()
    rows = len(seq1) + 1
    columns = len(seq2) + 1

    gap = 9

    matrix = [[0 for _ in range(columns)] for _ in range(rows)]

    for i in range(1, rows):
        for j in range(1, columns):
            a = matrix[i - 1][j - 1] + PAM250[seq1[i - 1]][seq2[j - 1]]
            b = matrix[i - 1][j] - gap
            c = matrix[i][j - 1] - gap
            matrix[i][j] = max(a, b, c)

    optimum_score = None
    for i in range(1, rows):
        for j in range(1, columns):
            if i == len(seq1) or j == len(seq2):
                if (optimum_score is None) or (optimum_score < matrix[i][j]):
                    optimum_score = matrix[i][j]
                else:
                    optimum_score = optimum_score

    seq = []
    for i in range(1, rows):
        if matrix[i][-1] == optimum_score:
            aligned_seq2 = '-' * (len(seq1[i:]))
            traceback(matrix, seq1, seq2, i, len(seq2), seq1[i:], aligned_seq2, aligned_pairs=seq)

    for j in range(1, columns - 1):
        if matrix[-1][j] == optimum_score:
            aligned_seq1 = '-' * (len(seq2[j:]))
            traceback(matrix, seq1, seq2, len(seq1), j, aligned_seq1, seq2[j:], aligned_pairs=seq)

    print(optimum_score)
    sortedSeq = [i[0] + i[1] for i in seq]
    sortedSeq.sort()
    for i in sortedSeq:
        print(i[0:int(len(i) / 2)])
        print(i[int(len(i) / 2):])


main()